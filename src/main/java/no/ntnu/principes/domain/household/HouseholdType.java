package no.ntnu.principes.domain.household;

/**
 * Defines the types of a household in the system.
 *
 * <ul>
 *   <li><strong>FAMILY</strong>: A household consisting of family members.</li>
 *   <li><strong>SHARED</strong>: A household shared by multiple individuals.</li>
 *   <li><strong>WORKPLACE</strong>: A household that represents a workplace.</li>
 * </ul>
 */
public enum HouseholdType {
  FAMILY, SHARED, WORKPLACE
}
